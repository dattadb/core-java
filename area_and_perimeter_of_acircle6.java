package Basic;

public class area_and_perimeter_of_acircle6 {
	
	
	public static void main(String[] args) {
		
		//area = πr
				//2 perimeter = 2πr
		int r=2;
	double pi=3.14;
		
		double area = pi*r;
				double  perimeter = 2*pi*r;
				
				System.out.println(area);
				System.out.println(perimeter);
	}

}
