package Basic;

public class Swap_with_third4 {
	
	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		System.out.println("After swap");
		System.out.println(a);
		System.out.println(b);
		
		int c=a;
		a=b;
		b=c;
		System.out.println("Before Swap");
		System.out.println(a);
		System.out.println(b);
	}

}
